// --- Utilidades Spotify ---
async function fetchSpotifyThumbnail(url) {
  if (!url) return "";
  try {
    const oembed = `https://open.spotify.com/oembed?url=${encodeURIComponent(url)}`;
    const res = await fetch(oembed);
    if (!res.ok) return "";
    const data = await res.json();
    return data.thumbnail_url || "";
  } catch { return ""; }
}

function buildSpotifyEmbed(url) {
  try {
    const u = new URL(url);
    const [, type, id] = u.pathname.split("/");
    if (!type || !id) return "";
    return `https://open.spotify.com/embed/${type}/${id}`;
  } catch { return ""; }
}

