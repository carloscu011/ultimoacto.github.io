const grid = document.getElementById("poemGrid");

(async function renderPoems() {
  // Orden por fecha ascendente
  poems.sort((a, b) => new Date(a.date) - new Date(b.date));

  for (const p of poems) {
    // Si hay Spotify y no hay imagen, intentar traer miniatura
    if (p.spotifyUrl && !p.image) {
      p.image = await fetchSpotifyThumbnail(p.spotifyUrl) || "";
    }

    const card = document.createElement("div");
    card.className = "card";
    card.dataset.id = p.id;

    const thumb = document.createElement("div");
    thumb.className = "thumb";
    thumb.innerHTML = p.image
      ? `<img src="${p.image}" alt="Portada">`
      : `<div class="noimg">Sin portada</div>`;

    const body = document.createElement("div");
    body.className = "body";
    body.innerHTML = `
      <div class="title">${p.title}</div>
      <div class="date">${p.date}</div>
      <div class="excerpt">${p.content}</div>
    `;

    const actions = document.createElement("div");
    actions.className = "actions";
    const leerBtn = document.createElement("button");
    leerBtn.className = "btn btn-primary";
    leerBtn.textContent = "Leer";
    leerBtn.onclick = (e) => { e.stopPropagation(); openModal(p); };

    actions.appendChild(leerBtn);

    if (p.spotifyUrl) {
      const a = document.createElement("a");
      a.className = "btn btn-dark";
      a.textContent = "Spotify";
      a.href = p.spotifyUrl;
      a.target = "_blank";
      a.rel = "noopener";
      a.id = `open-${p.id}`;
      actions.appendChild(a);
    }

    card.appendChild(thumb);
    card.appendChild(body);
    card.appendChild(actions);

    card.addEventListener("click", () => openModal(p));
    grid.appendChild(card);
  }
})();

// --- Modal ---
const modal = document.getElementById("modal");
const modalTitle = document.getElementById("modalTitle");
const modalDate = document.getElementById("modalDate");
const modalContent = document.getElementById("modalContent");
const modalImage = document.getElementById("modalImage");
const modalPlayer = document.getElementById("modalPlayer");
const btnSpotify = document.getElementById("btnSpotify");

function openModal(p) {
  modalTitle.textContent = p.title;
  modalDate.textContent = p.date;
  modalContent.textContent = p.content;

  modalImage.src = p.image || FOTO_MANUAL;

  modalPlayer.innerHTML = "";
  if (p.spotifyUrl) {
    const src = buildSpotifyEmbed(p.spotifyUrl);
    if (src) {
      const iframe = document.createElement("iframe");
      iframe.src = src;
      iframe.allow = "autoplay; clipboard-write; encrypted-media; fullscreen; picture-in-picture";
      iframe.width = "100%";
      iframe.height = "152";
      iframe.loading = "lazy";
      modalPlayer.appendChild(iframe);
    }
    btnSpotify.href = p.spotifyUrl;
    btnSpotify.classList.remove("hidden");
  } else {
    btnSpotify.classList.add("hidden");
  }

  modal.classList.add("show");
  document.body.style.overflow = "hidden";
}

function closeModal() {
  modal.classList.remove("show");
  document.body.style.overflow = "auto";
  modalPlayer.innerHTML = "";
}

document.getElementById("modalClose").addEventListener("click", closeModal);
document.getElementById("btnVolver").addEventListener("click", closeModal);
modal.addEventListener("click", (e) => { if (e.target === modal) closeModal(); });